package ensa.project_vt;

public class VideoInf {
    private String title;
    private String id;
    private String subnotsub;
    private String date;
    private String size;

    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubnotsub() {
        return subnotsub;
    }

    public void setSubnotsub(String subnotsub) {
        this.subnotsub = subnotsub;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }


}
